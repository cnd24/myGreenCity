        <h3> Description </h3>

          <div class="description">
            <p><strong>1-terre-action</strong> est une association s'inscrivant dans une démarche liée au <strong>développement durable.</strong> </p>
            
            <p>Les bénévoles collectent et réparent des vélos inutilisés ou promis à la destruction, et leur redonnent une seconde vie, réduisont les 
            déchets et profitant de petits prix pour promouvoir l'usage du vélo. </p>
            <p>L'association propose également un service d'auto-réparation à tous ses adhérents.</p>
            <p>L'idée forte est de proposer l'accès aux ateliers, aux outils et aux conseils d'un technicien cycle et de pouvoir s'acheter des pièces détachées, 
            neuves ou d'occasions, à prix prédéfini et <strong> raisonnable </strong>.</p>
            <p>Les bénévoles oeuvrent pour moins de voiture en ville donc moins de pollution et de stress, donc plus de bonheur !</p>
            <p>L'association a également une facette sociale, ayant pu créer des emplois pour assurer le quotidien des permanences, ateliers d'auto-réparation, 
            gestion des vélos, recherche de partenariat, de projets etc.</p>
            <p>Mais rien ne serait possible sans l'implication des bénévoles qui sont indispensables au fonctionnement de l'association. Si cela vous tente, 
            n'hésitez pas à les rejoindre.</p>
          </div>
