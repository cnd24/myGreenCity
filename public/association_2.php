        <h2> Description </h2>
        <p> Re-boot est une association à vocation sociale et solidaire dans le domaine de l'informatique en général, des 
          logiciels libres et de Linux en particulier. </p>
         <p> Elle a été créée le 28 octobre 2010 par des personnes passionnées d'informatique et se situe à la Ferté Saint-Aubin. </p>
          <p>L'association est fondée sur les valeurs de partage, de respect de la différence, de la liberté d'expression et de l'écoute. </p>
          <p>Le premier principe général est celui qui est souvent cité dans le monde du libre :"si tu ne sais pas, demande si tu sais, partage", 
          Re-boot a pour ambition, comme bien d'autres associations, de promouvoir le logiciel libre en général et Linux en particulier. Le principe de 
          base est donc de partager son savoir pour que l'autre puisse apprendre et partager à nouveau. </p>
          <p>L'autre principe général est celui qui énonce que "le chemin est long mais la voie est libre". En effet, se défaire des habitudes n'est 
            pas chose facile, apprendre demande des efforts. Pour accompagner ceux qui veulent effectuer le changement du monde fermé au monde libre, 
            l'association et tous ses membres s'engagent à prendre un peu de temps (en fonction de leurs disponibilités et/ou de leur envies) pour 
            les autres.</p>
          <p>L'association ambitionne de collecter des ordinateurs anciens (et au besoin des périphériques) pour les convertir au libre via 
            l'installation d'un OS Linux (et optimiser les composants si possible) afin de les rendre à nouvau disponibles pour ceux qui en ont 
            besoin (peu de moyens financiers par exemple). </p>
      </section>
